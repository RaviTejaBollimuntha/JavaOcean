package com.rt.bo;

import java.sql.Date;

public class OrderBO {
	private	String Fname,Lname,Email,Address,City,Country,product,amount,noi,Moblie,Pincode;
    private Date d;
    
	public OrderBO(String fname, String lname, String email,String mobile, String city,String pincode, String country, String address,
			String product, String amount, String noi,Date d) {
		this.Fname = fname;
		this.Lname = lname;
		this.Email = email;
		this.Address = address;
		this.City = city;
		this.Country = country;
		this.product = product;
		this.amount = amount;
		this.noi = noi;
		this.Moblie = mobile;
		this.Pincode = pincode;
		this.d = d;
	}

	public String getFname() {
		return Fname;
	}

	public void setFname(String fname) {
		Fname = fname;
	}

	public String getLname() {
		return Lname;
	}

	public void setLname(String lname) {
		Lname = lname;
	}

	public String getEmail() {
		return Email;
	}

	public void setEmail(String email) {
		Email = email;
	}

	public String getAddress() {
		return Address;
	}

	public void setAddress(String address) {
		Address = address;
	}

	public String getCity() {
		return City;
	}

	public void setCity(String city) {
		City = city;
	}

	public String getCountry() {
		return Country;
	}

	public void setCountry(String country) {
		Country = country;
	}

	public String getProduct() {
		return product;
	}

	public void setProduct(String product) {
		this.product = product;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getNoi() {
		return noi;
	}

	public void setNoi(String noi) {
		this.noi = noi;
	}

	public String getMoblie() {
		return Moblie;
	}

	public void setMoblie(String moblie) {
		Moblie = moblie;
	}

	public String getPincode() {
		return Pincode;
	}

	public void setPincode(String pincode) {
		Pincode = pincode;
	}

	public Date getD() {
		return d;
	}

	public void setD(Date d) {
		this.d = d;
	}
	
	
}
